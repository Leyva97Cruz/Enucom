package com.mx.general;

public class Planetas {
	
	String Nombre;
	int NumeroDePlaneta;
	double Masa;
	String Vida;
	public String getNombre() {
		return Nombre;
	}
	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	public int getNumeroDePlaneta() {
		return NumeroDePlaneta;
	}
	public void setNumeroDePlaneta(int numeroDePlaneta) {
		NumeroDePlaneta = numeroDePlaneta;
	}
	public double getMasa() {
		return Masa;
	}
	public void setMasa(double masa) {
		Masa = masa;
	}
	public String getVida() {
		return Vida;
	}
	public void setVida(String vida) {
		Vida = vida;
	}
	

}
